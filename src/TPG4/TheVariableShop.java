package TPG4;

public class TheVariableShop {
    public static void main(String[] args) {
        int theInteger = 1;
        double theDouble = 1.0;
        float theFloat = 1.00F;
        boolean theBoolean = true;
        char theChar = 'A';
        byte theByte = 1;
        short theShort = 1;
        long theLong = 1;
        String theString = "Hello";

        System.out.println("This is an integer: " + theInteger);
        System.out.println("This is a double: " + theDouble);
        System.out.println("This is a float: " + theFloat);
        System.out.println("This is a boolean: " + theBoolean);
        System.out.println("This is a character: " + theChar);
        System.out.println("This is a byte: " + theByte);
        System.out.println("This is a short: " + theShort);
        System.out.println("This is a long: " + theLong);
        System.out.println("This is a string: " + theString);

        theInteger = 2;
        theDouble = 2.0;
        theFloat = 2.00F;
        theBoolean = false;
        theChar = 'B';
        theByte = 2;
        theShort = 2;
        theLong = 2;
        theString = "Goodbye";

        System.out.println("This is another integer: " + theInteger);
        System.out.println("This is another double: " + theDouble);
        System.out.println("This is another float: " + theFloat);
        System.out.println("This is another boolean: " + theBoolean);
        System.out.println("This is another character: " + theChar);
        System.out.println("This is another byte: " + theByte);
        System.out.println("This is another short: " + theShort);
        System.out.println("This is another long: " + theLong);
        System.out.println("This is another string: " + theString);

    }
}
